package nwt.kts.backend.dto.login;

public class FacebookLoginData {

    private String value;

    private String name;

    private String surname;

    public FacebookLoginData() {
    }

    public String getValue() {
        return value;
    }

    public String getName() {
        return name;
    }

    public String getSurname() {
        return surname;
    }
}
